#define RGB_LED_R &MODULE_P02, 7  // Digital pin  9 - P02.7
#define RGB_LED_G &MODULE_P10, 5  // Digital pin 10 - P10.5
#define RGB_LED_B &MODULE_P10, 3  // Digital pin 11 - P10.3

/*********************************************************************************************************************/
/*-------------------------------------------------Global variables--------------------------------------------------*/
/*********************************************************************************************************************/
uint32   g_counter1ms;  // 1ms 카운터
TaskFlag g_taskFlags;   // 작업 실행 준비 상태를 나타내는 플래그 집합

/*********************************************************************************************************************/
/*------------------------------------------------Function Prototypes------------------------------------------------*/
/*********************************************************************************************************************/
static void excuteTask1ms(void);
static void excuteTask10ms(void);
static void excuteTask100ms(void);
static void excuteTask1000ms(void);

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
// General Purpose Input/Output (GPIO)
void initGPIO(void)
{
    // RGB LED 핀을 출력 모드로 설정
    IfxPort_setPinMode(RGB_LED_R, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(RGB_LED_G, IfxPort_Mode_outputPushPullGeneral);
    IfxPort_setPinMode(RGB_LED_B, IfxPort_Mode_outputPushPullGeneral);
}

// 작업 스케줄링 함수
void excuteTasks(void)
{
    // 플래그 값에 따라 작업 실행
    if (g_taskFlags.isTask1msReady)
    {
        g_taskFlags.isTask1msReady = FALSE;
        excuteTask1ms();
    }

    if (g_taskFlags.isTask10msReady)
    {
        g_taskFlags.isTask10msReady = FALSE;
        excuteTask10ms();
    }

    if (g_taskFlags.isTask100msReady)
    {
        g_taskFlags.isTask100msReady = FALSE;
        excuteTask100ms();
    }

    if (g_taskFlags.isTask1000msReady)
    {
        g_taskFlags.isTask1000msReady = FALSE;
        excuteTask1000ms();
    }
}

static void excuteTask1ms(void)
{
    // 작업 없음
}

static void excuteTask10ms(void)
{
    // ADC 값의 가능한 범위(12비트 해상도)를 4개의 구간으로 나눔
    Color colorIndex = readADCValue() >> 10;

    // ADC 값이 속하는 구간에 따라 RGB LED 제어
    switch (colorIndex)
    {
        case Color_None:
        {
            IfxPort_setPinState(RGB_LED_R, IfxPort_State_low);
            IfxPort_setPinState(RGB_LED_G, IfxPort_State_low);
            IfxPort_setPinState(RGB_LED_B, IfxPort_State_low);
            break;
        }
        case Color_Red:
        {
            IfxPort_setPinState(RGB_LED_R, IfxPort_State_high);
            IfxPort_setPinState(RGB_LED_G, IfxPort_State_low);
            IfxPort_setPinState(RGB_LED_B, IfxPort_State_low);
            break;
        }
        case Color_Green:
        {
            IfxPort_setPinState(RGB_LED_R, IfxPort_State_low);
            IfxPort_setPinState(RGB_LED_G, IfxPort_State_high);
            IfxPort_setPinState(RGB_LED_B, IfxPort_State_low);
            break;
        }
        case Color_Blue:
        {
            IfxPort_setPinState(RGB_LED_R, IfxPort_State_low);
            IfxPort_setPinState(RGB_LED_G, IfxPort_State_low);
            IfxPort_setPinState(RGB_LED_B, IfxPort_State_high);
            break;
        }
    }
}

